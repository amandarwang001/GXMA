                        <div class="col">
                            David Cheng has never stopped his martial arts research, training, and teaching. He is
                            frequently invited to do seminars in Europe and America, and he has also taught seminars in
                            Germany, Spain, Canada, Indonesia, Japan, Hongkong, and South Korea. In 2016, he established
                            the Guo Xing Martial Art School (國興武術學校) with branches in Brisbane, Australia and Florida,
                            USA.
                            <br><br>
                            The depth and breadth of traditional Chinese martial arts is coveted by many martial artists
                            from other styles, and they have often come to visit David Cheng for instruction. So, in the
                            martial arts community in Australia, David Cheng has earned the nickname: “the martial arts
                            corrector.” Some of his visiting students from other styles include: a former Australian
                            Olympic Super Heavyweight boxer [boxing], an Australian Immigration and Border Protection
                            Security guard [from Henan Xingyi Quan], and an Australian Navy Special Forces member [from
                            Mian Quan], etc.
                            <br><br>
                            Apart from teaching martial arts seminars, David Cheng has also been interviewed by Channel
                            V’s host Jian Zhou Chen, famous DJ Wing from UniFM96.7 [with Facebook live], and Hongkong’s
                            Kung Fu Quest.
