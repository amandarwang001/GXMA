                    <div class="row">
                        <div class="col">
                            <p>
                                When he was in 16, his family immigrated from New Zealand to Australia. Through an
                                introduction from a friend, David Cheng met the Taiwan National Team coach, Shipo Chang,
                                and he began his journey of discovery of Chinese internal and external martial arts. He
                                flew to and from Australia numerous times to learn from Shipo Chang. Whenever he had a
                                slightly longer vacation, he would fly to Taiwan to learn more. The variety of Chinese
                                martial art styles is very large and in order to continue what Master Fa Cheng passed
                                down to him, David Cheng studied Hebei Liu Qi Lan Xingyi from Shipo Chang. Later,
                                because his abilities did not yet meet the height of Chinese internal martial arts, he
                                turned to study Shandong’s Master Sun Shao Tang’s system of Shaolin. At 18 he was
                                selected as a member of the Australian National Kungfu Wushu Team (required score of
                                at least 16) with a score of 62
                                points. And in 1998, David Cheng represented Australia in the ISKA World Cup Martial
                                Arts Championship and won a World Cup silver medal. After his win at the Championship,
                                he was employed as a Shaolin instructor by the then-biggest Chinese martial arts
                                association of Australia, the Australian Chinese Bagua Taichi Association.
                                <br><br>
                                In 2000, David Cheng attended the Beijing International Martial Arts Invitational
                                Qualifying Tournament, winning both gold and bronze medals. Until he graduated from
                                university, he traveled to and from Australian and Taiwan to focus on learning and
                                teaching Xingyi and Shaolin. In 2002, after graduating from university, in order to have
                                more time to study from Master Shipo Chang, David Cheng gave up the opportunity to study
                                for his Master’s degree and instead returned to Taiwan.
                                <br><br>
                                In his 3rd year of university, David Cheng pursued body building and injured his 4th
                                vertebrae, causing pain and continuous nose bleeds. Doctors told him that a surgery was
                                needed to correct the injury, but his father was against it because he was too young. So
                                he tried both Western medicine and Traditional Chinese Medicine, but both failed to
                                alleviate the pain. However, after studying Taiji from Master Shipo Chang, the problem
                                was cured without any medical help. Only then did David Cheng realize the value of the
                                combined theories of training and personal cultivation in Chinese martial arts. He
                                wished to help more people, so he started teaching Taiji. In 2005, Shipo Chang
                                recognized the improvement and level of David Cheng’s martial arts skills, so he
                                introduced David Cheng to the cradle of Taiwanese martial artists: the National Taiwan
                                College of Performing Arts. David was hired as a martial arts instructor at the college
                                and Shipo Chang started teaching David Cheng Gao Style Bagua Zhang and Qi Family
                                Tongbei.
                                <br><br>
                                In 2007, David Cheng was appointed by Master Shipo Cheng to represent Taiwan and
                                participate in the World Cup of Martial Arts in Hong Kong. David Cheng won two gold
                                medals in the tournament and began to feel the importance of striving towards inheriting
                                the art.
                                <br><br>
                                During his study, David Cheng followed Shipo Chang’s systematic education and emphasis
                                on inheriting the martial arts as his main focus of training. He was also fortunate to
                                be introduced to and influenced by instruction from Qinsun Ding’s Yang Family Taiji and
                                also the broad martial arts knowledge of Kangyi Liu, who is like a friend and like a
                                teacher to him as well as the owner of Lion Books. Under the guidance and generous
                                teaching of these two people, David Cheng’s comprehensive understanding of Chinese
                                martial arts as a whole was greatly and positively influenced.
                                <br><br>
                                In 2006, with the goal of contributing to the lineage and promoting it in Western
                                countries, David Cheng began translating the Gao Style of Cheng School Bagua Zhang
                                Manual [published on 25th April, 2008], under the tutelage of Shipo Chang’s first
                                disciple, Yukai Chang [David Cheng’s current Shifu].
                                <br><br>
                                In 2009, Yukai Chang was promoted to the Chinese Martial Arts Research Director of the
                                National Taiwan Sports University, with David Cheng helping as his research assistant.
                                Under this close and frequent contact and because they were of similar mind with the
                                same intentions and goals of working towards promoting the art, in 2014 David Cheng was
                                accepted into the lineage and became the First Disciple of Yukai Chang, and the 9th
                                (Guo/國) generation lineage holder of Gao Style of Cheng School Bagua Zhang. At his
                                discipleship ceremony he was given the martial arts name Guo Xing (國興) by his Shifu.
