                <div class="col m9 grey lighten-4">
                    <p class="flow-text">
                        Shifu David Cheng is a 9th generation lineage holder of Gao style Cheng school Bagua Zhang, 10th
                        generation lineage holder of Hebei Xinygi Quan, 7th generation lineage holder of Yang style Taiji Quan,
                        and 7th generation lineage holder of Northern
                        Shaolin. He is the First Disciple of Shifu Chang Yukai, Ph.D.
                    </p>
                    <div class="row">
                        <div class="col m6">
                            David Cheng (鄭謙), martial arts name Guo Xing (國興), was born in Taipei, Taiwan in 1981. When
                            he was seven, he was influenced by his father and movies, and began learning Tae Kwon Do and
                            Chin Na, leading to a gradually increasing interest in the martial arts. A few years later,
                            his family moved to New Zealand. Because he immigrated at such a young age, he cherished and
                            highly regarded traditional Chinese culture. When he was 10, he met Master Fa Cheng and
                            Master Tien Ruo, who had stopped in New Zealand while proselytizing around the world. With
                            them, he began learning more about traditional Chinese martial arts and culture, including
                            Shaolin, Hebei Sun Lu Tang Xingyi, and Yang Family Taiji, Buddhism, and Ruism. Although his
                            understanding of these things was at an early stage, it demonstrated the uniqueness of
                            Chinese culture and continued to inspire David Cheng’s interest in martial arts.
                            <br><br>
                            Although he was young, he was more patient and persistent than all the other adults in the
                            class and soon became the inheritor of the manual from Master Fa Cheng. After a short year,
                            Master Fa Cheng had to move on to his next destination, South Africa, to continue his
                            proselytization and David Cheng had to stop his studying. Although he continued to
                            self-study, his young age, the maturity that wisdom requires, and having no master prevented
                            him from improving.
