                    <p>
                        The Association’s missions are to promote strengthening the mind and body through classical Chinese martial arts, and sharing Chinese culture and philosophy with communities worldwide.
                        <br>
                        <br> Since the primary goal of students of traditional Chinese martial arts is self-improvement, our mission statement centers on:
                    <ul>
                        <li>&#8226; Self-improvement through a balance of physical, mental, and social perspectives.</li>
                        <li>&#8226; Development of moral virtues.</li>
                        <li>&#8226; Passing on the traditions of Chinese martial arts culture by training the mind and body together.</li>
                        <li>&#8226; Raising one’s quality of life physically, mentally, spiritually, and socially through martial training.</li>
                    </ul>
                    </p>
                    <p>
                        As such, students within the Association build their foundation of practice on the following four important principles:
                    <ol>
                        <li>Respect for others, which includes your teachers, parents, brothers and sisters, seniors and juniors, and the school.</li>
                        <li>Perseverance in all that one undertakes.</li>
                        <li>Appreciation of one’s life and cultivating an appreciative outlook.</li>
                        <li>The sharing of traditions and one’s time and knowledge with others.</li>
                    </ol>
                    </p>
